#     def __init__(self, x):
#         self.val = x
#         self.next = None

class Solution(object):
    def hasCycle(self, head):
        """
        :type head: ListNode
        :rtype: bool
        """
        slow=head
        fast=head

        while fast.next is not None and fast is not None:
            slow=slow.next
            fast=fast.next.next

            if slow.val==fast.val:
                return True
        return False

